# ===[ Imports: ]==============================================================
import pandas as pd


# ===[ nan_counter: ]==========================================================
def nan_counter(data: pd.DataFrame) -> dict:
    '''
    counts the number of missing values for each column and return it in dict
    '''
    count_dict = {}
    columns = data.columns
    for x in columns:
        count_dict[x] = data[x].isnull().sum()
        print(f"{x}: {data[x].isnull().sum()}")
    return count_dict
